const bolt = ( x, y ) => `<path d="M ${x} ${y - 26} l -8 16 l 9 2 l -7 16" fill="none" stroke="${C.red}" stroke-width="2.4"/>`;
const star = ( cx, cy, r ) => { const p = []; for ( let i = 0; i < 10; i++ ) {
	const a = -Math.PI / 2 + i * Math.PI / 5, rr = i % 2 ? r * 0.45 : r;
	p.push(`${cx + rr * Math.cos(a)},${cy + rr * Math.sin(a)}`); }
	return `<polygon points="${p.join(' ')}" fill="${C.green}" stroke="white" stroke-width="1.2"/>`; };
