// ── run the paper's deterministic harnesses (the figures' single data source) ────────────────────────────
async function loadData() {
	// E2 — §4.2 (N=80, two audited classes)
	const w2 = E.makeWorkload({ audited: [{ region: 'EU', kind: 'loan' }, { region: 'US', kind: 'wire' }],
		heldOutRegion: 'APAC', preCycles: 2, postCycles: 2 });
	const { results } = await H.runCampaign({ workload: w2, model: H.makeModel('stub') });
	DATA.e2 = { meta: w2.meta, rows: Object.fromEntries(results.map(( r ) => [r.name, r])) };
	const a = DATA.e2.rows;
	pin(w2.meta.n === 80 && a.STRUCT.calls === 26 && a.INVALIDATING.calls === 28 && a.CBR.calls === 24
		&& a.RAG.calls === 48 && a.NAIVE.calls === 80 && a.SKILL.calls === 80, 'E2 calls');
	pin(a.STRUCT.driftAcc === 1 && a.INVALIDATING.driftAcc === 1 && a.RAG.driftAcc === 0 && a.CBR.driftAcc === 0
		&& a.SKILL.driftAcc === 0 && a['LONG-CONTEXT'].maxContext === 2062 && a.STRUCT.maxContext === 290
		&& a.STRUCT.blames.length === 2, 'E2 drift/ctx/blames');

	// E6 — §4.7 (N=78, the named systems, most-favorable configs + ablations)
	const w6 = E.makeWorkload({ kinds: ['loan', 'refund', 'wire'], regions: ['EU', 'US', 'APAC'],
		heldOutRegion: 'APAC', audited: [{ region: 'EU', kind: 'loan' }, { region: 'US', kind: 'wire' }],
		preCycles: 2, postCycles: 3 });
	w6.feedback = makeFeedback(w6);
	const ALL6 = Object.assign({}, ARMS, NAMED_ARMS);
	DATA.e6 = { meta: w6.meta, rows: {} };
	for ( const name of ['NAIVE', 'MEMGPT', 'MEMGPT-BLIND', 'REFLEXION', 'REFLEXION-BLIND', 'GRAPHRAG', 'GRAPHRAG-REINDEX', 'STRUCT'] ) {
		const res = await ALL6[name](w6.stream, { workload: w6, model: H.makeModel('stub') });
		DATA.e6.rows[name] = Object.assign({ calls: res.calls, maxContext: res.maxContext }, H.score(res.actions, w6));
	}
	const b = DATA.e6.rows;
	pin(w6.meta.n === 78 && b.STRUCT.calls === 20 && b.MEMGPT.calls === 23 && b['MEMGPT-BLIND'].calls === 18
		&& b.REFLEXION.calls === 82 && b.GRAPHRAG.calls === 87 && b['GRAPHRAG-REINDEX'].calls === 89, 'E6 calls');
	pin(b.STRUCT.maxContext === 290 && b.MEMGPT.maxContext === 320 && b.REFLEXION.maxContext === 332
		&& b.GRAPHRAG.maxContext === 336 && b['MEMGPT-BLIND'].maxContext === 258, 'E6 ctx');
	DATA.e6.dominators = Object.keys(DATA.e6.rows).filter(( n ) => n !== 'STRUCT'
		&& DATA.e6.rows[n].calls <= b.STRUCT.calls && DATA.e6.rows[n].driftAcc === 1
		&& DATA.e6.rows[n].maxContext <= b.STRUCT.maxContext);
	pin(DATA.e6.dominators.length === 0, 'E6 Pareto uniqueness');

	// E7 — §4.8 (the two-link chain decide→disburse, same audit cascading)
	const w7 = W.makeComposedWorkload({ kinds: ['loan', 'refund', 'wire'], regions: ['EU', 'US', 'APAC'],
		heldOutRegion: 'APAC', audited: [{ region: 'EU', kind: 'loan' }, { region: 'US', kind: 'wire' }],
		preCycles: 2, postCycles: 3 });
	w7.feedback = CNA.makeFeedback(w7);
	const ALL7 = Object.assign({}, COMPOSED_SURFACE_ARMS, CNA.COMPOSED_NAMED_ARMS);
	DATA.e7 = { meta: w7.meta, rows: {} };
	for ( const name of ['NAIVE-2', 'CBR-2', 'MEMGPT-2', 'REFLEXION-2', 'GRAPHRAG-2-REINDEX', 'STRUCT-2'] ) {
		const res = await ALL7[name](w7.stream, { workload: w7, model: CH.makeModel('stub') });
		DATA.e7.rows[name] = Object.assign({ calls: res.calls }, CH.score(res, w7));
	}
	const c = DATA.e7.rows;
	pin(c['STRUCT-2'].calls === 38 && c['CBR-2'].calls === 36 && c['NAIVE-2'].calls === 156
		&& c['MEMGPT-2'].calls === 45 && c['REFLEXION-2'].calls === 160 && c['GRAPHRAG-2-REINDEX'].calls === 178, 'E7 calls');
	pin(c['STRUCT-2'].driftAcc1 === 1 && c['STRUCT-2'].driftAcc2 === 1
		&& c['CBR-2'].driftAcc1 === 0 && c['CBR-2'].driftAcc2 === 0, 'E7 drift');

	// P4 — §4.5 (the K1-coverage gradient, both domains, real canon barrier)
	DATA.p4 = { approval: P4.sweep('approval', 200), triage: P4.sweep('triage', 200) };
	pin(DATA.p4.approval.map(( r ) => Math.round(r.amort * 100)).join() === '0,19,44,69,94'
		&& DATA.p4.triage.map(( r ) => Math.round(r.amort * 100)).join() === '0,22,47,72,97'
		&& DATA.p4.approval.every(( r ) => r.structAcc === 1) && DATA.p4.triage.every(( r ) => r.structAcc === 1), 'P4 gradient');

	// E8 — §4.9 (revision under recurrent drift, K=5, categorical + numeric)
	DATA.e8 = E8.measure(5);
	const cat = DATA.e8.rows[0], num = DATA.e8.rows[1];
	pin(cat.evict.totals.blames === 5 && cat.evict.totals.calls === 8
		&& cat.revise.totals.blames === 1 && cat.revise.totals.calls === 5
		&& num.evict.totals.blames === 5 && num.revise.totals.blames === 1, 'E8 totals');

	// F1 — the real contract fixtures + the closed-box verdicts (computed here)
	DATA.f1 = {
		norm: PM.METHODS.Normalize.contract, grade: PM.METHODS.Grade.contract,
		vOK:  CT.checkCompose(PM.METHODS.Normalize, PM.METHODS.Grade).verdict,
		vKO:  CT.checkCompose(PM.METHODS.BadNormalize, PM.METHODS.Grade).verdict,
		vESC: CT.checkCompose(PM.METHODS.Ship, PM.METHODS.Notify).verdict };
	pin(DATA.f1.vOK === 'sound' && DATA.f1.vKO === 'unsound' && DATA.f1.vESC === 'escalate', 'E3 verdicts');
}

